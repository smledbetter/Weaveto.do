/**
 * Generate a deterministic 2-word room name from a room ID
 *
 * @param roomId - Hex string room identifier (at least 4 chars)
 * @returns "adjective-noun" format (e.g. "swift-falcon")
 */
export function getRoomName(roomId: string): string {
  if (roomId.length < 4) {
    throw new Error("roomId must be at least 4 hex characters");
  }

  const adjIndex = parseInt(roomId.substring(0, 2), 16);
  const nounIndex = parseInt(roomId.substring(2, 4), 16);

  if (
    isNaN(adjIndex) ||
    isNaN(nounIndex) ||
    adjIndex < 0 ||
    nounIndex < 0 ||
    adjIndex > 255 ||
    nounIndex > 255
  ) {
    throw new Error("Invalid hex bytes in roomId");
  }

  return `${ADJECTIVES[adjIndex]}-${NOUNS[nounIndex]}`;
}
